//AND operator
let x = 10
let y = 10
if(x <= y && x == y)
{
    console.log("true")
}else{
    console.log("false")
}

//OR operator
let xx = 10
let yy = 20
if(xx <= yy || xx == yy)
{
    console.log("true")
}else{
    console.log("false")
}

//NOT operator
let a = false
if(!a){
    console.log("true")
}
else{
    console.log("false")
}
     
